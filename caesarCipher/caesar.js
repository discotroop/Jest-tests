const caesar = {
    add: function(num, num2) {
        return num + num2;
    },
    subtract: function (num, num2) {
        return num - num2; 
    },
    divide: function (num, num2) {
        return num / num2;
    },
    multiply: function (num, num2) {
        return num * num2;
    }

}

module.exports = caesar;